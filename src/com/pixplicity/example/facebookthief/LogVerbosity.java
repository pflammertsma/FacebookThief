package com.pixplicity.example.facebookthief;

/**
 * Represents log verbosity. In decreasing order (i.e. increasing specificity):
 * <ul>
 * <li>{@code V} (verbose)</li>
 * <li>{@code D} (debug)</li>
 * <li>{@code I} (info)</li>
 * <li>{@code W} (warning)</li>
 * <li>{@code E} (error)</li>
 * </ul>
 */
public enum LogVerbosity {
	V, D, I, W, E
}
